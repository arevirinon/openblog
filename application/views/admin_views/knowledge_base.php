<h2>Welcome to OpenBlog cPanel</h2>

<p>By default, you are directed to the Dashboard page where you can see brief statistics from the system.</p>

<hr>

<p>The sidebar (on your left) has the list of all the main pages.</p>
<ul>
	<li><strong>Blog Entries</strong> lists the blog posts submitted from the public site.</li>
	<li>You can check the list of users who have registered from the public site via the <strong>Authors</strong> page. Authors are categorized to two types: Public Author and Administrator. Only users under Administrator are allowed to access the cPanel. Also, Administrators can update registered users' role and status.</li>
	<li>Administrators have the right to add a new category via the <strong>Blog Categories</strong> page.</li>
	<li><strong>Messages</strong> page shows the messages submitted from the Contact Form. Messages that are yellow-colored are the unread messages. </li>
	<li>Your profile information is shown in <strong>My Account</strong> page. Here, you can also view the number of blog posts you have submitted.</li>
</ul>





